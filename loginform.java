    private String getCurrentDate() {
        try {
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","system","dinesh07");
            String sql = "SELECT TO_CHAR(SYSDATE, 'DD') AS current_date FROM dual";
            st = con.prepareStatement(sql);
            rs = st.executeQuery();
            if (rs.next()) {
                return rs.getString("current_date");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        } finally {
            // Close your database resources (ResultSet, Statement, Connection) in a finally block
            try {
                if (rs != null) {
                    rs.close();
                }
                if (st != null) {
                    st.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        return null;
    }

