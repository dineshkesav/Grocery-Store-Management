    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
        con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "system", "dinesh07");

        String sql = "UPDATE demployees SET ename=?, salary=?, mobileno=?, gender=? WHERE eid=?";
        st = con.prepareStatement(sql);
        st.setString(1, jTextField2.getText()); // ename
        st.setDouble(2, Double.parseDouble(jComboBox1.getSelectedItem().toString())); // salary
        st.setString(3, jTextField3.getText()); // mobileno
        if (jRadioButton1.isSelected()) {
            st.setString(4, "MALE");
        } else if (jRadioButton2.isSelected()) {
            st.setString(4, "FEMALE");
        }
        st.setInt(5, Integer.parseInt(jTextField1.getText())); // eid
        int rowsUpdated = st.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(null, "Update successful");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jComboBox1.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "No record found for the provided Employee ID");
        }
    } catch (HeadlessException | SQLException ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

